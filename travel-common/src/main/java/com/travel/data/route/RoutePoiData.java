package com.travel.data.route;

public class RoutePoiData {

	private int dayIdx;
	private int subIdx;
	private String poiid;
	private String name;
	public int getDayIdx() {
		return dayIdx;
	}
	public void setDayIdx(int dayIdx) {
		this.dayIdx = dayIdx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSubIdx() {
		return subIdx;
	}
	public void setSubIdx(int subIdx) {
		this.subIdx = subIdx;
	}
	public String getPoiid() {
		return poiid;
	}
	public void setPoiid(String poiid) {
		this.poiid = poiid;
	}
}
